----CURSORES
DECLARE
ESTATUS VARCHAR2(20);
CURSOR CUR1 IS SELECT * FROM DANCE;   --DECLARACION DE CURSOR
BEGIN
FOR REC IN CUR1 LOOP
IF REC.EDAD<18 THEN 
  ESTATUS:='PRECOS';
DBMS_OUTPUT.PUT_LINE('|NOMBRE| '||REC.NOMBRE||' |EDAD| '||REC.EDAD||' |ESTATUS| '||ESTATUS);
ELSE 
ESTATUS:='VIEJO';
DBMS_OUTPUT.PUT_LINE(' |NOMBRE| '||REC.NOMBRE||' |EDAD| '||REC.EDAD||' |ESTATUS| '||ESTATUS);
END IF;-- SE CIERRA EL IF
END LOOP;--SE CIERRA EL CURSOR
END;
